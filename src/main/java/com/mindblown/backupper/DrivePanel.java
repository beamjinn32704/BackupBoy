    private void regButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_regButtonMouseClicked
        if(!Register.hasRegistry(drive)){
            register.register(drive);
        } else {
            if(isRegistered()){
                RegistryEditPanel pan = new RegistryEditPanel(register.getRegistry(drive));
                JOptionPane pane = new JOptionPane(pan, JOptionPane.PLAIN_MESSAGE);
                editPanelDial = pane.createDialog(this, "Edit " + drive.getRoot() + "'s Registry");
                editPanelDial.setVisible(true);
            } else {
                register.addRegistry(drive);
            }
        }
        setFields();
    }//GEN-LAST:event_regButtonMouseClicked
    
    public void disposeEdit(){
        register.deregister(drive);
        editPanelDial.dispose();
        Main.main.cacheRegister();
    }
    
