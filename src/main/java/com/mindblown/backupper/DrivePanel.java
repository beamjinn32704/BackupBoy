    private String getButtonText(){
        boolean hasRegistry = Register.hasRegistry(drive);
        if(hasRegistry){
            if(isRegistered()){
                return "Edit Registration";
            } else {
                return "Add Registry";
            }
        } else {
            return "Register";
        }
    }
    
    private String getLabelText(){
        if(isRegistered()){
            return "Registered";
        } else {
            return "UnRegistered";
        }
    }
    
    private boolean isRegistered(){
        return register.registered(drive);
    }
    
